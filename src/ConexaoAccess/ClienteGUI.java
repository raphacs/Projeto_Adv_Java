    private void salvarBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarBTNActionPerformed
     String nome,telefone,parcelas,pagas,deve,table, valor_parcelas; 
     nome = txtnome.getText();
     telefone = txttel.getText();
     deve = txtdeve.getText();
     valor_parcelas = txtvparcela.getText();
     pagas = txtpagas.getText();
     parcelas = txtparcelas.getText();
     
     
     
     
     try{
           if(con == null){
               Class.forName(driver);
               con = DriverManager.getConnection(url);
               pst = con.prepareStatement("insert into record(NOME,NUMERO,VALOR_TOTAL,VALOR_DA_PARCELA,PARCELAS_PAGAS,PARCELAS_ABERTAS) values(?,?,?,?,?,?) ");
               pst.setString(1, nome);
               pst.setString(2, telefone);
               pst.setString(3, deve);
               pst.setString(4, valor_parcelas);
               pst.setString(5, pagas);
               pst.setString(6, parcelas);
               
                       }
       } catch(Exception ex){
           ex.printStackTrace();
           con = null;
       }


     
    }//GEN-LAST:event_salvarBTNActionPerformed

